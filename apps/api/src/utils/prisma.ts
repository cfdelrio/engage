// eslint-disable-next-line @typescript-eslint/no-explicit-any
export function asJson(value: unknown): any {
  // eslint-disable-next-line @typescript-eslint/no-explicit-any
  return value as any;
}

// eslint-disable-next-line @typescript-eslint/no-explicit-any
export function asJsonNullable(value: unknown): any {
  // eslint-disable-next-line @typescript-eslint/no-explicit-any
  return value as any;
}
